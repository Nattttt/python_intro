# Introducción al lenguaje de programacion Python
# Lección 1
# Tipos de datos primitivos

# Numéricos
entero_positivo = 5
print(entero_positivo, type(entero_positivo))

entero_negativo = -1
print(entero_negativo, type(entero_negativo))

flotante = 1.35
print(flotante, type(flotante))

complejo = 3 + 1.5j
print(complejo, type(complejo))

# Cadenas
a = "Bienvenido a Python"
print(a, type(a))

b = 'Bienvenido a Python'
print(b, type(b))

# Booleanos
veradero = True
print(veradero, type(veradero))

falso = False
print(falso, type(falso))
